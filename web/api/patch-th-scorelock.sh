#!/bin/bash
# 泰语 scoreLock 升级脚本

FILE="ai-advisor.js"

# 备份
cp "$FILE" "${FILE}.bak2"

# 用 awk 进行多行替换
awk '
/^      : lang === '\''th'\''$/ {
  print
  getline
  if (/^\      \? `\[ข้อมูลบังคับ/) {
    print "      ? `[ข้อมูลบังคับ ห้ามตัด ห้ามเปลี่ยน ห้ามสร้างเอง]\\nคะแนนรวม = ${overall}/100\\n${baziScore ? `คะแนนบาซี = ${baziScore}/100` : '\'''\''}\\n${zodiacScore ? `คะแนนราศี = ${zodiacScore}/100` : '\'''\''}\\n${ichingScore ? `คะแนนอี้จิง = ${ichingScore}/100` : '\'''\''}\\nไพ่ทาโรต์ = \"${tarot?.name || '\'''\''} ${tarotOrient}\"\\n${!ichingScore ? '\''หมายเหตุ: คะแนนอี้จิง ให้ค้นหา "I Ching Score" หรือ "คะแนนอี้จิง" ในส่วน [I CHING]'\'' : '\'''\''}\\n\\n[ข้อบังคับในบทที่ 4]\\n- ต้องกลับมาอ้างคะแนนทั้ง 4 ดวงชะตาให้ครบถ้วน: บาซี / ราศี / อี้จิง / ไพ่ทาโรต์\\n- ห้ามตัดทิ้ง ห้ามสร้างคะแนนเอง ห้ามใช้คะแนนอื่นนอกจากนี้\\n- ห้ามนำคะแนนจาก "ราดาร์ 4 มิติ" (dims) มาใช้ในบทที่ 4 — นั่นคือคะแนนย่อยเฉพาะด้าน ไม่ใช่คะแนนระบบหลัก\\n- หากคะแนนอี้จิง = 60 แต่ไพ่ทาโรต์ = 82 ต้องอธิบายความขัดแย้งนี้อย่างมีเหตุผล ห้ามละเลย\\n\\n`"
    next
  }
}
{ print }
' "${FILE}.bak2" > "$FILE"

echo "✅ 泰语 scoreLock 已升级"
